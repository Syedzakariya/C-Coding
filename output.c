/*Output
integers  - printf("age is %d",age);
real numbers - printf("value of pi is %f",pi);
characters - printf("star looks like this %c",star);
*/
#include<stdio.h>
int main()
{
    int age=18;
    printf("my age is %d\n",age);
    float pi=3.14;
    printf("the value of pi is %f\n",pi);
    char star='*';
    printf("star looks like this %c\n",star);   
return 0;
}  